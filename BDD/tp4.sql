CREATE VIEW SALLE_POSTE(NOM_S, NOM_P, N_SEGMENT, AD, TYPE_P)
AS
SELECT S.NOM_S, P.NOM_P, P.N_SEGMENT, P.AD, P.TYPE_P
FROM POSTE AS P, SALLE AS S 
WHERE P.N_SALLE = S.N_SALLE

