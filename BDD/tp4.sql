CREATE VIEW POSTE_0(NPOS0, NSAL0, TYP0, NSEG0, A0)
AS
SELECT NOM_P, N_SALLE, TYPE_P, N_SEGMENT, AD
FROM POSTE
WHERE N_SEGMENT = (SELECT N_SEGMENT
                    FROM SEGMENT 
                    WHERE ETAGE = 0);

