CREATE VIEW SALLE_PRIX(N_SALLE, NOM_S, NB_POSTE, PRIX_LOCATION)
AS
SELECT N_SALLE, NOM_S, NB_POSTE, (100 * NB_POSTE)
FROM SALLE 
GROUP BY N_SALLE

